package Chapter2;

public class LongExample {
    public static void main(String[] args) {
        long var1 = 10;
        long var2 = 20L;
//        long var3 = 100000000000; int형을 long에 넣는 것이라 불가능
        long var4 = 100000000000L;
        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var4);
    }
}
